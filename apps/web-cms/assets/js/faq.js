  function escapeHTML(value) {
    return String(value)
      .replace(/&/g, "&amp;")
      .replace(/</g, "&lt;")
      .replace(/>/g, "&gt;")
      .replace(/"/g, "&quot;")
      .replace(/'/g, "&#039;");
  }

  function getSection(id) {
    return sections.find(function (section) {
      return section.id === id;
    }) || sections[0];
  }

  function renderFaqItems(section) {
    var list = document.getElementById("faq-list");
    if (!list) return;

    list.innerHTML = section.items.map(function (item, index) {
      return [
        '<div class="faq-item" data-faq-item>',
          '<button class="faq-item__button" type="button" aria-expanded="false">',
            '<span class="faq-item__question">' + escapeHTML(item.q) + '</span>',
            '<span class="faq-item__icon" aria-hidden="true">',
              '<svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round">',
                '<polyline points="6 9 12 15 18 9"></polyline>',
              '</svg>',
            '</span>',
          '</button>',
          '<div class="faq-item__answer" hidden>',
            '<div class="faq-item__answer-inner">',
              '<p>' + escapeHTML(item.a) + '</p>',
            '</div>',
          '</div>',
        '</div>'
      ].join("");
    }).join("");

    list.querySelectorAll("[data-faq-item]").forEach(function (item) {
      var button = item.querySelector(".faq-item__button");
      var answer = item.querySelector(".faq-item__answer");

      button.addEventListener("click", function () {
        var isOpen = item.classList.contains("active");

        list.querySelectorAll("[data-faq-item]").forEach(function (other) {
          other.classList.remove("active");
          var otherButton = other.querySelector(".faq-item__button");
          var otherAnswer = other.querySelector(".faq-item__answer");
          if (otherButton) otherButton.setAttribute("aria-expanded", "false");
          if (otherAnswer) otherAnswer.hidden = true;
        });

        if (!isOpen) {
          item.classList.add("active");
          button.setAttribute("aria-expanded", "true");
          answer.hidden = false;
        }
      });
    });
  }

  function activateSection(id, shouldScroll) {
    var section = getSection(id);

    document.querySelectorAll("[data-faq-section]").forEach(function (button) {
      var isActive = button.getAttribute("data-faq-section") === section.id;
      button.classList.toggle("active", isActive);
      if (isActive) {
        button.setAttribute("aria-current", "true");
      } else {
        button.removeAttribute("aria-current");
      }
    });

    var title = document.getElementById("faq-section-title");
    var breadcrumb = document.getElementById("faq-breadcrumb-current");

    if (title) title.textContent = section.label;
    if (breadcrumb) breadcrumb.textContent = section.label;

    renderFaqItems(section);

    if (shouldScroll) {
      var content = document.getElementById("faq-content-top");
      if (content) {
        window.scrollTo({
          top: content.getBoundingClientRect().top + window.scrollY - 100,
          behavior: "smooth"
        });
      }
    }
  }

  function initFaqPage() {
    document.querySelectorAll("[data-faq-section]").forEach(function (button) {
      button.addEventListener("click", function () {
        var id = button.getAttribute("data-faq-section") || "general";
        var url = new URL(window.location.href);
        url.searchParams.set("section", id);
        window.history.pushState({ section: id }, "", url.toString());
        activateSection(id, true);
      });
    });

    window.addEventListener("popstate", function () {
      var params = new URLSearchParams(window.location.search);
      activateSection(params.get("section") || "general", false);
    });

    var params = new URLSearchParams(window.location.search);
    activateSection(params.get("section") || "general", false);
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", initFaqPage);
  } else {
    initFaqPage();
  }
