function renderEvent(event: string): React.ReactNode {
  const parts = event.split(
    /((?:\d{4}(?:–\d{4})?\s*гг?\.)|(?:\d{4}\s*г\.)|(?:\d{1,2}\s+[а-яё]+\s*—))/giu
  );

  return parts.map((part, i) =>
    /(?:\d{4}(?:–\d{4})?\s*гг?\.)|(?:\d{4}\s*г\.)|(?:\d{1,2}\s+[а-яё]+\s*—)/iu.test(part) ? (
      <strong key={i} className="font-bold">
        {part}
      </strong>
    ) : (
      part
    )
  );
}

